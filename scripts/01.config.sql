-- Altera o container para o PDBORCL21
ALTER SESSION SET CONTAINER = PDBORCL21;

-- Cria o tablespace TBS_SOURCE
CREATE TABLESPACE tbs_source 
    DATAFILE '/u01/app/oracle/oradata/ORCL21/pdborcl21/TBS_SOURCE.dbf'
    SIZE 1M
    AUTOEXTEND ON NEXT 10M
    MAXSIZE UNLIMITED
    EXTENT MANAGEMENT LOCAL
    SEGMENT SPACE MANAGEMENT AUTO;

-- Cria o usu치rio source
CREATE USER source IDENTIFIED BY source001 DEFAULT TABLESPACE tbs_source;
GRANT CONNECT, RESOURCE TO source;
GRANT UNLIMITED TABLESPACE TO source;

